import UIKit

extension UIColor {
       static var ypBlack: UIColor { UIColor(named: "black") ?? UIColor.black }
       static var ypGray: UIColor { UIColor(named: "gray") ?? UIColor.gray }
       static var ypWhite: UIColor { UIColor(named: "white") ?? UIColor.white }
}
